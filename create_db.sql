
CREATE TABLE CUSTOMER(
 CUSTID INT,
 GENDER varchar2(20),
 MARRIED BOOLEAN,
 EDUCATION varchar2(20),
 INCOME FLOAT,
 CREDIT_SCORE INT
);

CREATE TABLE LOAN_APP(
LOAN_APP_ID INT,
CUSTID INT,
LOAN_AMMOUNT FLOAT,
APPLICATION_DATE DATE,
APP_STATUS varchar2(10)
);

CREATE TABLE LOAN(
LOANID INT,
LOAN_APP_ID INT,
APPROVED_AMOUNT FLOAT,
INTEREST_RATE FLOAT,
START_DATE DATE
);

CREATE TABLE LOAN_PAYMENT(
LOANID INT,
PAYMENT_DATE DATE,
AMMOUNT_PAYED FLOAT
);

CREATE TABLE RISK(
LOANID INT,
RISK_SCORE FLOAT,
APPROVE varchar2(1)
);


